/* Numbers

1 - toString
2 - toFixed
3 - toPrecision
*/


var number = 105.798
console.log('Numero: ', number)

// 1 - toString

console.log('ToString: ', number.toString())

// 2 - toFixed

console.log('ToFixed: ', number.toFixed(1))

// 3 - toPrecision

console.log('ToPrecision: ', number.toPrecision(6))